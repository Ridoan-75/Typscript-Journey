// "?" ‡¶¶‡¶ø‡ßü‡ßá Optional Parameter ‡¶¨‡ßã‡¶ù‡¶æ‡¶®‡ßã ‡¶π‡ßü
function greet(name: string, title?: string): string {
    if (title) {
        return `Hello, ${title} ${name}`;
    }
    return `Hello, ${name}`;
}

